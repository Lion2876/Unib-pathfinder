$nodeMap = [];
foreach ($nodes as $n) {
    $nodeMap[$n['code']] = $n;
}

$edgeDataArray = [];
foreach ($edges as $e) {
    $from = $nodeMap[$e['from']];
    $to = $nodeMap[$e['to']];

    $url = "http://router.project-osrm.org/route/v1/foot/{$from['lng']},{$from['lat']};{$to['lng']},{$to['lat']}?geometries=geojson";
    
    $ch = curl_init();
    curl_setopt($ch, CURLOPT_URL, $url);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, 1);
    $response = curl_exec($ch);
    curl_close($ch);
    
    $path = null;
    $data = json_decode($response, true);
    if (isset($data['routes'][0]['geometry']['coordinates'])) {
        $coords = $data['routes'][0]['geometry']['coordinates'];
        $path = array_map(function($c) {
            return [ round($c[1], 6), round($c[0], 6) ];
        }, $coords);
    }

    $pathStr = $path ? json_encode($path) : 'null';
    $pathStr = str_replace('"', '', $pathStr);
    
    $line = "            ['from' => '{$e['from']}', 'to' => '{$e['to']}', 'dist' => {$e['dist']}, 'base' => {$e['base']}, 'peak' => {$e['peak']}, 'zone' => '{$e['zone']}'";
    if ($path) {
        $line .= ", 'path' => $pathStr";
    }
    $line .= "],";
    
    $edgeDataArray[] = $line;
    usleep(500000); 
}

file_put_contents('edges_output.txt', implode("\n", $edgeDataArray));
